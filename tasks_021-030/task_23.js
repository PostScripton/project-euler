/*
Идеальным числом называется число, у которого сумма его делителей равна самому числу.
Например, сумма делителей числа 28 равна 1 + 2 + 4 + 7 + 14 = 28, что означает, что число 28 является идеальным числом.

Число n называется недостаточным, если сумма его делителей меньше n, и называется избыточным, если сумма его делителей больше n.

Так как число 12 является наименьшим избыточным числом (1 + 2 + 3 + 4 + 6 = 16), наименьшее число, которое может быть записано как сумма двух избыточных чисел, равно 24.
Используя математический анализ, можно показать, что все целые числа больше 28123 могут быть записаны как сумма двух избыточных чисел. Эта граница не может быть уменьшена дальнейшим анализом,
даже несмотря на то, что наибольшее число, которое не может быть записано как сумма двух избыточных чисел, меньше этой границы.

Найдите сумму всех положительных чисел, которые не могут быть записаны как сумма двух избыточных чисел.
*/

function isAbundant(num) {
	if (num < 12) return false
	const divisor = +(num / 2) + 1

	let sum = 0
	for (let i = 1; i <= divisor; i++)
		if (num % i === 0) sum += i

	return sum > num
}

const limit = 28123
let abundants = []
let sum_of_abundants = []
let result = 0

for (let i = 12; i < limit; i++) {
	if (isAbundant(i)) {
		abundants.push(i)

		abundants.forEach(abundant => {
			const sum = abundant + i
			sum_of_abundants[sum] = true
		})
	}
}

for (let i = 1; i <= limit; i++)
	if (!sum_of_abundants[i]) result += i

console.log('Result:', result) //OUTPUT: 4179871